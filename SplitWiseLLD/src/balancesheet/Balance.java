package balancesheet;

public class Balance {
    double owe;
    double getBack;

    public double getOwe() {
        return owe;
    }

    public void setOwe(double owe) {
        this.owe = owe;
    }

    public double getGetBack() {
        return getBack;
    }

    public void setGetBack(double getBack) {
        this.getBack = getBack;
    }
}
